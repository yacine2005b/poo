    public class point {
    private float x;
    private float y;


    public point(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public float getX() {
        return x;
    }


    public float getY() {
        return y;
    }


    public void setX(float newX) {
        x = newX;
    }


    public void setY(float newY) {
        y = newY;
    }
}


